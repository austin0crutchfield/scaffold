"""Basic calculator module."""

def addIntegers(num1: int, num2: int) -> int:
    """Adds two numbers.
    
    Args:
        num1 (int) - first number to add
        num2 (int) - second number to add (to the first)

    Returns:
        int - num1 added to num2
    """
    return num1 + num2



def subtractIntegers(num1: int, num2: int) -> int:
    """Subtract two numbers.
    
    Args:
        num1 (int) - the number to subtract value from
        num2 (int) - the amount to subtract

    Returns:
        int - num1 less num2
    """
    return num1 - num2